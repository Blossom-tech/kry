package se.kry.codetest.migrate;

import io.vertx.core.Vertx;
import se.kry.codetest.DBConnector;

public class DBMigration {

  public static void main(String[] args) {
    Vertx vertx = Vertx.vertx();
    DBConnector connector = new DBConnector(vertx);
    connector.query("CREATE table if not EXISTS service (id integer PRIMARY KEY AUTOINCREMENT, name varchar(250) not NULL, url varchar(250) not NULL, created_at datetime not NULL DEFAULT CURRENT_TIMESTAMP, status TINYINT);").setHandler(done -> {
      if(done.succeeded()){
        System.out.println("completed db migrations");
      } else {
        done.cause().printStackTrace();
      }
      vertx.close(shutdown -> {
        System.exit(0);
      });
    });
  }
}
